package org.noob.codewars.train.leetcode.dataStructure.linkedlist.reverse_linked_list_206;

public class ListNode {
    int val;

    ListNode next;

    public ListNode() {

    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
